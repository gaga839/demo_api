package com.wocai.jrt.utils;

public class WordFormatter {
	private String ContractNumber;// 合同编号
	private String InvestorName;// 投资人姓名
	private String InvestorSignName;// 投资人签名
	private String SignTime;// 签单时间
	private String Amount;// 投资金额
	private String AmountToCN;// 投资金额大写
	private String IDCard;// 证件号
	private String Address;// 通信地址
	private String Phone;// 联系电话
	private String Email;// 邮箱
	private String AccountName;// 开户名称
	private String BankName;// 开户银行
	private String BankAccount;// 银行帐（卡）号

	public String getContractNumber() {
		return ContractNumber;
	}

	public void setContractNumber(String contractNumber) {
		ContractNumber = contractNumber;
	}

	public String getInvestorName() {
		return InvestorName;
	}

	public void setInvestorName(String investorName) {
		InvestorName = investorName;
	}

	public String getInvestorSignName() {
		return InvestorSignName;
	}

	public void setInvestorSignName(String investorSignName) {
		InvestorSignName = investorSignName;
	}

	public String getSignTime() {
		return SignTime;
	}

	public void setSignTime(String signTime) {
		SignTime = signTime;
	}

	public String getAmount() {
		return Amount;
	}

	public void setAmount(String amount) {
		Amount = amount;
	}

	public String getIDCard() {
		return IDCard;
	}

	public void setIDCard(String iDCard) {
		IDCard = iDCard;
	}

	public String getAddress() {
		return Address;
	}

	public void setAddress(String address) {
		Address = address;
	}

	public String getPhone() {
		return Phone;
	}

	public void setPhone(String phone) {
		Phone = phone;
	}

	public String getEmail() {
		return Email;
	}

	public void setEmail(String email) {
		Email = email;
	}

	public String getAccountName() {
		return AccountName;
	}

	public void setAccountName(String accountName) {
		AccountName = accountName;
	}

	public String getBankName() {
		return BankName;
	}

	public void setBankName(String bankName) {
		BankName = bankName;
	}

	public String getBankAccount() {
		return BankAccount;
	}

	public void setBankAccount(String bankAccount) {
		BankAccount = bankAccount;
	}

	public String getAmountToCN() {
		return AmountToCN;
	}

	public void setAmountToCN(String amountToCN) {
		AmountToCN = amountToCN;
	}

}
